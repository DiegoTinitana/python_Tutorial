'''
Un lambda es una pequena funcion anonima que retorna algo
'''
#Ejemplo

x = lambda a: a + 10 # las funciones lambda simpre iran con la palabra reservada 'lambda'
print(x(5))

#lambda con mas argumentos 
x = lambda a, b : a * b
print(x(5, 6))

